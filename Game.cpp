bool g_bIsPaused = false;

//=====================================================================
// 初期化処理
//=====================================================================
void InitGame(void)
{
	InitField();
	InitWall();
	InitShadow();
	InitPlayer();
	InitBillboard();
	InitBullet();
	InitExplosion();
	InitEffect();

	g_bIsPaused = false;

	LoadScript("data\\model.txt");
}

//=====================================================================
// 終了処理
//=====================================================================
void UninitGame(void)
{
	UninitField();
	UninitWall();
	UninitPlayer();
	UninitShadow();
	UninitBillboard();
	UninitBullet();
	UninitExplosion();
	UninitEffect();
}

//=====================================================================
// 更新処理
//=====================================================================
void UpdateGame(void)
{
	if (GetKeyboardTrigger(DIK_P))
	{
		g_bIsPaused ^= 1;
	}

	if (g_bIsPaused == false)
	{
		UpdateField();
		UpdateWall();
		UpdatePlayer();
		UpdateShadow();
		UpdateBillboard();
		UpdateBullet();
		UpdateExplosion();
		UpdateEffect();
	}
}

//=====================================================================
// 描画処理
//=====================================================================
void DrawGame(void)
{
	DrawField();
	DrawWall();
	DrawPlayer();
	DrawShadow();
	DrawBillboard();
	DrawBullet();
	DrawExplosion();
	DrawEffect();
}